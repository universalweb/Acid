/**
 * Adds two numbers.
 *
 * @function add
 * @category math
 * @type {Function}
 * @param {number} number - First number.
 * @param {number} value - Second number.
 * @returns {number} - Returns the sum of the arguments.
 *
 * @example
 * import { add, assert } from 'Acid';
 * assert(add(1, 1), 2);
 */
export function add(source, value) {
	return source + value;
}
